// smoke_test.S — 冒烟测试汇编程序
//
// 单周期 RV32I CPU 测试 — 验证基本指令:
//   ADDI, ADD, BNE, SW, LUI, JAL
//
// 地址  | 机器码      | 汇编指令               | 说明
// ------|------------|------------------------|------------------
// 0x00  | 00500093   | ADDI x1, x0, 5        | x1 = 5
// 0x04  | 00300113   | ADDI x2, x0, 3        | x2 = 3
// 0x08  | 002081B3   | ADD  x3, x1, x2       | x3 = 5 + 3 = 8
// 0x0C  | 00800213   | ADDI x4, x0, 8        | x4 = 8 (期望值)
// 0x10  | 00419063   | BNE  x3, x4, 0x10     | x3==x4, 不跳转
// 0x14  | 00100293   | ADDI x5, x0, 1        | x5 = 1 (PASS)
// 0x18  | 10000237   | LUI  x4, 0x10000      | x4 = 0x1000_0000 (用于基地址)
//                            — 但 Phase 1 中 DMEM 地址空间小,
//                              实际上 x4 的高位会被截断
// 0x1C  | 10502023   | SW   x5, 0x100(x0)   | mem[0x100] = 1 (PASS flag)
//                            — DMEM 字地址 = 0x100>>2 = 0x40
// 0x20  | 0000006F   | JAL  x0, 0            | 死循环 (自跳转)
//
// 验证:
//   TB 监控 DMEM 地址 0x40 (= 字节地址 0x100 >> 2)
//   写入 1 → PASS, 写入其他 → FAIL
//
// hex 文件:
//   00500093
//   00300113
//   002081B3
//   00800213
//   00419063
//   00100293
//   10000237
//   10502023
//   0000006F
//
// 注意: 原来的 smoke_test.hex 用的 AUIPC + ADDI + SW 模式是
//   正确的, 但这里简化为 LUI + SW 以便更好理解。
//   你任选一种即可, 只要最终 DMEM[0x40] 被写入 1 就行。
