//   rvcpu_immgen.v — 立即数生成器 (纯组合逻辑)
//
//   从 32 位指令中提取并符号扩展立即数。
//   RV32I 有 6 种立即数编码格式: I/S/B/U/J 及移位指令的 shamt。
//
// 你也可以选择在译码器 (rvcpu_decode.v) 中直接生成立即数,
//   不需要独立的 immgen 模块。但独立出来可以让每个模块职责更清晰。
//
// 6 种立即数格式图解:
//
//   I-type (ADDI, LOAD, JALR):
//     [31:20] → {{20{sign}}, [31:20]}
//
//   S-type (STORE):
//     [31:25] + [11:7] → {{20{sign}}, [31:25], [11:7]}
//
//   B-type (BRANCH):
//     [31] + [7] + [30:25] + [11:8] + 1'b0
//     → {{19{sign}}, [31], [7], [30:25], [11:8], 1'b0}
//
//   U-type (LUI, AUIPC):
//     [31:12] + 12'b0 → {[31:12], 12'b0}
//
//   J-type (JAL):
//     [31] + [19:12] + [20] + [30:21] + 1'b0
//     → {{11{sign}}, [31], [19:12], [20], [30:21], 1'b0}
//
// 你的任务:
//   Step 1: 提取 opcode 和立即数位域
//   Step 2: 生成 5 种格式的立即数候选值
//   Step 3: 根据 opcode 选择正确的输出
//
// 注意: 移位指令 SLLI/SRLI/SRAI 的立即数是 5 位 shamt,
//       位于 instr[24:20], 作为无符号数使用。
//       在 RISC-V RV32I 中, 其他立即数位 (instr[31:25]) 用于区分 SRLI/SRAI,
//       不是立即数的一部分。ALU 在使用时只取 op2[4:0]。
//
//       对于 Phase 1, 为简化, 移位指令的立即数可以直接返回到 instr[24:20],
//       ALU 会用 op2[4:0] 取低 5 位。
//==============================================================================

